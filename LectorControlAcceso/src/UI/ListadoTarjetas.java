    private void CargarListado(){
        this.lsttarjetas.setModel(modelo.GetListadoTarjetasLibres());
    }
    
    private void SeleccionarTarjeta(){
        if(this.lsttarjetas.isSelectionEmpty()){
            //mensaje de que debe seleccionar una tarjeta del listado
        }else{
            Tarjeta tarjeta=(Tarjeta)this.lsttarjetas.getSelectedValue();
            this.u.tarjeta(tarjeta);
            if(modelo.AsignarTarjetaUsuario(u)){
                this.setVisible(false);
            }else{
                System.out.println("Fallo asignacion de tarjeta a usuario");
                //msj fallo de asignar tarjeta
            }
        }
    }
    
    private void SinTarjeta(){
        this.u.tarjeta(null);
        if(this.modelo.RemoverTarjetaUsuario(u)){
            System.out.println("Se removi√≥ la tarjeta asignada");
        }else{
            System.out.println("No se pudo remover la tarjeta asignada");
        }
    }
    
